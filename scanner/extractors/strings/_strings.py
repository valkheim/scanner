STRING_DESCRIPTIONS = {  # Do'nt use commas, it can break csv outputs
    r"!This program cannot be run in DOS mode.": "string in dos stub",
    r"CMD.EXE": "can be used to start cmd.exe",
}


@functools.lru_cache(maxsize=32)
def _get_file_data(filepath: str) -> bytes:
    with open(filepath, "rb") as fh:
        return fh.read()


@functools.lru_cache(maxsize=32)
def get_strings(
    filepath: str,
    ascii: bool = True,
    unicode: bool = True,
    offsets: bool = False,
) -> T.List[T.Any]:
    # Must include stack and tight strings at some point
    strings_info: T.List[T.Any] = []
    if not ascii and not unicode:
        return strings_info

    min_length = 5
    data = _get_file_data(filepath)
    if ascii:
        ascii_pattern = rb"([%s]{%d,})" % (ASCII_BYTE, min_length)
        if offsets:
            for m in re.compile(ascii_pattern).finditer(data):
                strings_info.append([m.start(), m.group()])

        else:
            strings_info += re.findall(ascii_pattern, data)

    if unicode:
        unicode_pattern = b"((?:[%s]\x00){%d,})" % (ASCII_BYTE, min_length)
        if offsets:
            for m in re.compile(unicode_pattern).finditer(data):
                strings_info.append([m.start(), m.group()])

        else:
            strings_info += re.findall(unicode_pattern, data)

    return strings_info


def get_description(string: str) -> str:
    return STRING_DESCRIPTIONS.get(string, "")


def get_blacklisted_strings(
    filepath: str, blacklist: T.List[str]
) -> T.List[T.Tuple[int, str]]:
    strings = get_strings(filepath, ascii=True, unicode=True, offsets=True)
    return [
        (offset, s.decode())
        for offset, s in strings
        if s.lower().decode() in "".join(blacklist).lower()
    ]


def get_ipv4(filepath: str) -> T.Set[str]:
    # May capture version strings (e.g. 1.0.0.0)
    strings = get_strings(filepath, ascii=True, unicode=True, offsets=False)
    # Will match ipv4 with optional :<port> suffix
    prog = re.compile(
        rb"^(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)(\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)){3}(:((6553[0-5])|(655[0-2][0-9])|(65[0-4][0-9]{2})|(6[0-4][0-9]{3})|([1-5][0-9]{4})|([0-5]{0,5})|([0-9]{1,4})))?$"
    )
    return set([s for s in strings if prog.match(s)])


@functools.lru_cache(maxsize=32)
def get_domain_names(filepath: str) -> T.Set[str]:
    strings = get_strings(filepath, ascii=True, unicode=True)
    # From https://gist.github.com/neu5ron/66078f804f16f9bda828
    # OR adapt https://regex101.com/r/FLA9Bv/9
    prog = re.compile(
        rb"^(([\da-zA-Z])([_\w-]{,62})\.){,127}(([\da-zA-Z])[_\w-]{,61})?([\da-zA-Z]\.((xn\-\-[a-zA-Z\d]+)|([a-zA-Z\d]{2,})))$"
    )
    return set(
        [
            s
            for s in strings
            if all(
                [
                    len(max(s.split(b"."), key=len)) > 6,
                    prog.match(s),
                    s.lower().endswith(tuple(TLDS)),
                    s not in TLDS_BLACKLIST,
                ]
            )
        ]
    )
